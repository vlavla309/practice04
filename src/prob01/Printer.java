package prob01;

public class Printer {
	public void println(int param) {
		System.out.println(param);
	}
	public void println(boolean param) {
		System.out.println(param);
	}
	public void println(double param) {
		System.out.println(param);
	}
	public void println(String param) {
		System.out.println(param);
	}
	
}
